// push(); : 배열에 맨 마지막 값으로 추가하고자 할때
// pop(); : 배열에 맨 마지막 값을 제거
// shift(); : 배열에 맨 척번째 값을 제거
// unshift(); : 배열에 맨 척번째 값을 추가

let animals = ["lion", "bear", "bird"];
console.log(animals);

animals.pop();
console.log(animals);

animals.push("tiger");
console.log(animals);

animals.shift();
console.log(animals);

animals.unshift("rabbit");
console.log(animals);
